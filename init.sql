SET NAMES utf8mb4;

-- 1. メニューテーブル (旧 meat_parts)
CREATE TABLE menu (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(100),
  description TEXT,
  taste TEXT,
  recommended_method TEXT
);

-- 2. コメントテーブル
CREATE TABLE comments (
  id INT AUTO_INCREMENT PRIMARY KEY,
  menu_id INT NULL,
  username VARCHAR(100),
  age_group VARCHAR(50),
  comment TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 3. アンケート投票用テーブル
CREATE TABLE votes (
  id INT AUTO_INCREMENT PRIMARY KEY,
  menu_id INT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

